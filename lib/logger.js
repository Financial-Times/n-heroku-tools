function log(args, color){


	let msg = util.format.apply(null, args);
	if(color){
		msg = colors[color](msg);
	}

	console.log(msg);
}

function logArt(art, color){
	if(process.env.CI){
		return;
	}

	console.log(colors[color](art));
}

